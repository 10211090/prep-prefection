package com.papb.prepperfection.group;

public class Carts {
    String userId, cartId, productId, nameItem, qtyItem, priceItem, statusItem, tglItem;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCartId() {
        return cartId;
    }

    public void setCartId(String cartId) {
        this.cartId = cartId;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getNameItem() {
        return nameItem;
    }

    public void setNameItem(String nameItem) {
        this.nameItem = nameItem;
    }

    public String getQtyItem() {
        return qtyItem;
    }

    public void setQtyItem(String qtyItem) {
        this.qtyItem = qtyItem;
    }

    public String getPriceItem() {
        return priceItem;
    }

    public void setPriceItem(String priceItem) {
        this.priceItem = priceItem;
    }
    public String getStatusItem() {
        return statusItem;
    }

    public void setStatusItem(String statusItem) {
        this.statusItem = statusItem;
    }
    public String getTglItem() {
        return tglItem;
    }

    public void setTglItem(String tglItem) {
        this.tglItem = tglItem;
    }
}
